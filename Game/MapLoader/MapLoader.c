void DrawMap(Texture2D floorTexture, Texture2D wallTexture)
{
    for (int y = 0; y < MAP_HEIGHT; y++)
    {
        for (int x = 0; x < MAP_WIDTH; x++)
        {
            int tile = map1[y][x];

            Rectangle dest = {
                (float)(x * TILE_SIZE),
                (float)(y * TILE_SIZE),
                (float)TILE_SIZE,
                (float)TILE_SIZE
            };

            if (tile == 0)
            {
                DrawTexturePro(
                    floorTexture,
                    (Rectangle){0, 0, (float)floorTexture.width, (float)floorTexture.height},
                    dest,
                    (Vector2){0, 0},
                    0.0f,
                    WHITE
                );
            }
            else if (tile == 1)
            {
                DrawTexturePro(
                    wallTexture,
                    (Rectangle){0, 0, (float)wallTexture.width, (float)wallTexture.height},
                    dest,
                    (Vector2){0, 0},
                    0.0f,
                    WHITE
                );
            }
        }
    }
}