void DrawMap(Texture2D tileset)
{
    float tileWidthOnScreen  = (float)GetScreenWidth() / MAP_WIDTH;
    float tileHeightOnScreen = (float)GetScreenHeight() / MAP_HEIGHT;

    for (int y = 0; y < MAP_HEIGHT; y++)
    {
        for (int x = 0; x < MAP_WIDTH; x++)
        {
            int tile = map1[y][x];

            int tileX = tile % TILESET_COLUMNS;
            int tileY = tile / TILESET_COLUMNS;

            Rectangle source = {
                (float)(tileX * SOURCE_TILE_SIZE),
                (float)(tileY * SOURCE_TILE_SIZE),
                (float)SOURCE_TILE_SIZE,
                (float)SOURCE_TILE_SIZE
            };

            Rectangle dest = {
                x * tileWidthOnScreen,
                y * tileHeightOnScreen,
                tileWidthOnScreen,
                tileHeightOnScreen
            };

            DrawTexturePro(
                tileset,
                source,
                dest,
                (Vector2){0.0f, 0.0f},
                0.0f,
                WHITE
            );
        }
    }
}